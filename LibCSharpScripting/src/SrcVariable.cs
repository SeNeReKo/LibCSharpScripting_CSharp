using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibCSharpScripting.src
{

	public class SrcVariable
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public SrcVariable(string typeName, string name)
		{
			this.TypeName = typeName;
			this.Name = name;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		public string TypeName
		{
			get;
			private set;
		}

		public string Name
		{
			get;
			private set;
		}

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public override string ToString()
		{
			return TypeName + " " + Name;
		}

	}

}
